    const toleData = {
    "Ward-7": ["Tole 7A", "Tole 7B", "Tole 7C"],
    "Ward-8": ["Tole 8A", "Tole 8B"],
    "Ward-15": ["Tole 15A", "Tole 15B", "Tole 15C"]
};

function updateToles() {
    const wardSelect = document.getElementById('edit-ward-no');
    const toleSelect = document.getElementById('edit-tole-name');
    const selectedWard = wardSelect.value;

    // Clear current options in the tole select
    toleSelect.innerHTML = '<option value="" disabled>Select Tole</option>';

    // Populate new toles based on selected ward
    if (selectedWard in toleData) {
        toleData[selectedWard].forEach(function(tole) {
            const option = document.createElement('option');
            option.value = tole;
            option.textContent = tole;
            toleSelect.appendChild(option);
        });
    }
}

